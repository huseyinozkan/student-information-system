    public void ders_giris_update(String ogrenci_no, String ders_adi, float _vize, float _final, float _butunleme) {
        
        NotGirisiRequest notGirisiRequest = new NotGirisiRequest();
        notGirisiRequest.setDersAdi(ders_adi);
        notGirisiRequest.setOgrenciNo(ogrenci_no);
        notGirisiRequest.setVize(String.valueOf(_vize));
        notGirisiRequest.setFinal(String.valueOf(_final));
        notGirisiRequest.setButunleme(String.valueOf(_butunleme));

        String json = Lib.jsonResponse("insert_update_not_girisi", new DataRequest(notGirisiRequest));
        DataResponse<NotGirisiResponse> dataResponse = Lib.gson.fromJson(json, new TypeToken<DataResponse<NotGirisiResponse>>() {
        }.getType());
        
    }
    
    public void not_girisi_liste() {

        defaultTableModel = (DefaultTableModel) tbl_notlar.getModel();

        NotGirisiRequest notGirisiRequest = new NotGirisiRequest();
        notGirisiRequest.setKullaniciAdi(this.kullanici_adi);
        notGirisiRequest.setDersAdi(this.secili_ders);

        String json = Lib.jsonResponse("not_girisi", new DataRequest(notGirisiRequest));
        DataResponse<NotGirisiResponse> dataResponse = Lib.gson.fromJson(json, new TypeToken<DataResponse<NotGirisiResponse>>() {
        }.getType());

        notGirisiResponseList = dataResponse.getData(this);
        if (notGirisiResponseList != null) {
            defaultTableModel.setRowCount(0);
            for (NotGirisiResponse notGirisiResponse : notGirisiResponseList) {
                Object[] objects = {
                    notGirisiResponse.getOgrenciNo(),
                    notGirisiResponse.getVize(),
                    notGirisiResponse.getFinal(),
                    notGirisiResponse.getButunleme()
                };
                defaultTableModel.addRow(objects);
            }
        }
    }

