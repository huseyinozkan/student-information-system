    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        defaultTableModel = (DefaultTableModel) tbl_notlar.getModel();

        boolean kayıtKontrol = false;

        for (int i = 0; i < tbl_notlar.getRowCount(); i++) {

            float _vize, _final, _butunleme;

            try {
                _vize = Float.parseFloat(defaultTableModel.getValueAt(i, 1).toString());
                _final = Float.parseFloat(defaultTableModel.getValueAt(i, 2).toString());
                _butunleme = Float.parseFloat(defaultTableModel.getValueAt(i, 3).toString());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Verileri Sadece Sayı Girebilirsiniz");
                break;
            }

            if ((_vize > 100 || _vize < 0) || (_final > 100 || _final < 0) || (_butunleme > 100 || _butunleme < 0)) {
                JOptionPane.showMessageDialog(this, "Hatalı Not Girişi");
                break;
            } else {
                kayıtKontrol = true;
            }

        }

        if (kayıtKontrol) {

            for (int i = 0; i < tbl_notlar.getRowCount(); i++) {

                float _vize, _final, _butunleme;
                String ogrenci_no;
                _vize = Float.parseFloat(defaultTableModel.getValueAt(i, 1).toString());
                _final = Float.parseFloat(defaultTableModel.getValueAt(i, 2).toString());
                _butunleme = Float.parseFloat(defaultTableModel.getValueAt(i, 3).toString());
                ogrenci_no = defaultTableModel.getValueAt(i, 0).toString();
                
                ders_giris_update(ogrenci_no, this.secili_ders, _vize, _final, _butunleme);
                
            }

            kayıtKontrol = false;
        }
        
        this.setVisible(false);


    }//GEN-LAST:event_jButton1ActionPerformed

