    public void bilgileri_getir(){
        List<OgretmenKimlikResponse> ogretmenKimlikResponseList;
        
        OgretmenKimlikRequest ogretmenKimlikRequest = new OgretmenKimlikRequest();
        ogretmenKimlikRequest.setKullaniciAdi(this.kullanici_adi);

        String json = Lib.jsonResponse("ogretmen_kimlik_list", new DataRequest(ogretmenKimlikRequest));
        DataResponse<OgretmenKimlikResponse> dataResponse = Lib.gson.fromJson(json, new TypeToken<DataResponse<OgretmenKimlikResponse>>() {
        }.getType());

        ogretmenKimlikResponseList = dataResponse.getData(this);
        if (ogretmenKimlikResponseList != null) {
            for (OgretmenKimlikResponse ogretmenKimlikResponse : ogretmenKimlikResponseList) {
                    lbl_ad_soyad.setText(ogretmenKimlikResponse.getAdSoyad());
                    lbl_brans.setText(ogretmenKimlikResponse.getBrans());
                    lbl_tc.setText(ogretmenKimlikResponse.getTc());
                    lbl_telefon.setText(ogretmenKimlikResponse.getTelefon());
            }
        }
    }
    
