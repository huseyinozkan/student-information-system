    private void ogrenciGuncelle() {
        if (tbl_ogrenci_list.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Lütfen Öğrenci Seçiniz!");
        } else {

            int row = tbl_ogrenci_list.getSelectedRow();

            String id = defaultTableModel.getValueAt(row, 0).toString();
            String no = defaultTableModel.getValueAt(row, 1).toString();
            String ad_soyad = defaultTableModel.getValueAt(row, 2).toString();
            String tc = defaultTableModel.getValueAt(row, 3).toString();
            String sifre = defaultTableModel.getValueAt(row, 4).toString();
            String telefon = defaultTableModel.getValueAt(row, 5).toString();
            String dogum_tarihi = defaultTableModel.getValueAt(row, 6).toString();
            String mail = defaultTableModel.getValueAt(row, 7).toString();
            String adres = defaultTableModel.getValueAt(row, 8).toString();
            String bolum = defaultTableModel.getValueAt(row, 9).toString();
            String sinif = defaultTableModel.getValueAt(row, 10).toString();
            String il = defaultTableModel.getValueAt(row, 11).toString();
            String ilce = defaultTableModel.getValueAt(row, 12).toString();

            OgrenciEditForm ogrenciEditForm = new OgrenciEditForm(null, true, this, id, no, ad_soyad, tc, sifre, telefon, dogum_tarihi, mail, adres, bolum, sinif, il, ilce);
            ogrenciEditForm.show();
            ogrenciList();
        }

    }

    private void btn_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guncelleActionPerformed
        ogrenciGuncelle();
    }//GEN-LAST:event_btn_guncelleActionPerformed

