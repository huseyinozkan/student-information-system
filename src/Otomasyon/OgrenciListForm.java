    public void ogrenciList() {
        String filtre = "%" + edt_filtre.getText() + "%";
        OgrenciRequest ogrenciRequest = new OgrenciRequest();
        ogrenciRequest.setFiltre(filtre);

        String json = Lib.jsonResponse("ogrenci_list", new DataRequest(ogrenciRequest));
        DataResponse<OgrenciResponse> dataResponse = Lib.gson.fromJson(json, new TypeToken<DataResponse<OgrenciResponse>>() {
        }.getType());

        ogrenciResponseList = dataResponse.getData(this);
        if (ogrenciResponseList != null) {
            defaultTableModel.setRowCount(0);
            for (OgrenciResponse calisanResponse : ogrenciResponseList) {
                Object[] objects = {
                    calisanResponse.getId(),
                    calisanResponse.getNo(),
                    calisanResponse.getAdSoyad(),
                    calisanResponse.getTc(),
                    calisanResponse.getSifre(),
                    calisanResponse.getTelefon(),
                    calisanResponse.getDogumTarihi(),
                    calisanResponse.getMail(),
                    calisanResponse.getAdres(),
                    calisanResponse.getBolum(),
                    calisanResponse.getSinif(),
                    calisanResponse.getIl(),
                    calisanResponse.getIlce()
                };
                defaultTableModel.addRow(objects);
            }
        }
    }

