    private void ogrenci_sil() {

        String id = defaultTableModel.getValueAt(tbl_ogrenci_list.getSelectedRow(), 0).toString();

        OgrenciRequest ogrenciRequest = new OgrenciRequest();
        ogrenciRequest.setId(id);
        String json = Lib.jsonResponse("delete_ogrenci", new DataRequest(ogrenciRequest));
        DataResponse<OgrenciResponse> dataResponse = Lib.gson.fromJson(json, new TypeToken<DataResponse<OgrenciResponse>>() {
        }.getType());

        ogrenciResponseList = dataResponse.getData(this);
        if (ogrenciResponseList != null) {
            ogrenciList();
        }

    }

    private void btn_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_silActionPerformed
        if (tbl_ogrenci_list.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(backForm, "Satır Seçiniz!");
        } else {
            int dialogResult = JOptionPane.showConfirmDialog(backForm, "Silmek İstediğinize Emin misiniz?", "Uyarı", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                ogrenci_sil();
            }
        }

    }//GEN-LAST:event_btn_silActionPerformed

